let opcao = parseInt(prompt("Digite um valor de 1 à 5"));

switch (opcao) {
    case 1:
        alert("você digitou o número 1")
        break;
    case 2:
        alert("você digitou o número 2")
        break;
    case 3:
        alert("você digitou o número 3")
        break;
    case 4:
        alert("você digitou o número 4")
        break;
    case 5:
        alert("você digitou o número 5")
        break;
    default:
        alert("Você digitou um valor inválido")

}